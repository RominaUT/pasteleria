    //Insertar
    void insertar(){        
        String nomProv = txtNombreProv.getText();        
        String correo = txtCorreoProv.getText();
        String direccion = txtDirecciónProv.getText();
        String idDistriProv =cboIdDistrito.getSelectedItem().toString();
        
        //Arreglo de tipo Object
        Object[] ob = new Object[4];        
        ob[0] = nomProv;
        ob[1] = correo;
        ob[2] = direccion;
        ob[3] = idDistriProv;
        dao.insertar(ob);
    }
    
    //Eliminar
    void eliminar(){
        int fila =tablaPrv.getSelectedRow();
        int ids=Integer.parseInt(tablaPrv.getValueAt(fila, 0).toString());
        if (fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        }else{
            dao.eliminar(ids);
        }
    }
    
    //Nuevo
    void nuevo(){
        txtNombreProv.setText("");
        txtCorreoProv.setText("");
        txtDirecciónProv.setText("");
        cboIdDistrito.setSelectedItem("");
        txtNombreProv.requestFocus();
        
    }
    
    //Actualizar
    void actualizar(){
        int Fila=tablaPrv.getSelectedRow();
        if(Fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        }else{
            String nomProv = txtNombreProv.getText();        
            String correo = txtCorreoProv.getText();
            String direccion = txtDirecciónProv.getText();
            String idDistriProv =cboIdDistrito.getSelectedItem().toString();
            Object[] obj = new Object[5];       
            obj[0]=nomProv;
            obj[1]=correo;        
            obj[2]=direccion;
            obj[3]=idDistriProv;
            obj[4]=idProv;
            dao.actualizar(obj);            
        }        
    }
    
    void limpiarTabla(){
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i=i-1;
        }
    }
