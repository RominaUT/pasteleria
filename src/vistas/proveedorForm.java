    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // Obtener el texto ingresado por el usuario en el campo de búsqueda
        String NomProve = txtConsultaProveedor.getText().trim();

        // Verificar si se ha ingresado un nombre para buscar
        if (!NomProve.isEmpty()) {
            // Limpiar la tabla antes de agregar los resultados de la búsqueda
            limpiarTabla();

            // Llamar al método buscarPorNombre y obtener la lista de resultados
            List<Proveedor> resultados = dao.buscarPorNombre(NomProve);

            // Verificar si se encontraron resultados
            if (!resultados.isEmpty()) {
                // Llenar la tabla con los resultados de la búsqueda
                modelo = (DefaultTableModel) tablaPrv.getModel();
                Object[] ob = new Object[6];

                for (Proveedor ins : resultados) {
                    ob[0] = ins.getIdProv();
                    ob[1] = ins.getNomProv();
                    ob[2] = ins.getCorreo();
                    ob[3] = ins.getDireccion();
                    ob[4] = ins.getIdDistriProv();
                    ob[5] = ins.getNomDistri();
                    modelo.addRow(ob);
                }

                tablaPrv.setModel(modelo);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre para buscar.");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed
