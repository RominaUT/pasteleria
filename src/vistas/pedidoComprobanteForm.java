    //Metodos para los botones
    //Insertar
    void insertar(){              
        String id_Cli = cboCliente.getSelectedItem().toString();
        String id_Usua = cboEmpleado.getSelectedItem().toString();
        String fecEmi = txtFechaPedido.getText();
        String prePedC = txtPrecio.getText();
        String cantPedCli = txtCantidad.getText();
        String producto = txtProducto.getText();
        String desc = txtDescuento.getText();
        
        //Arreglo de tipo Object
        Object[] ob = new Object[7];        
        ob[0] = id_Cli;
        ob[1] = id_Usua;
        ob[2] = producto;
        ob[3] = prePedC;
        ob[4] = desc;
        ob[5] = cantPedCli;
        ob[6] = fecEmi;
        daoPC.insertar(ob);
    }
    
    //Eliminar
    void eliminar(){
        int fila =tablaPC.getSelectedRow();
        int ids=Integer.parseInt(tablaPC.getValueAt(fila, 0).toString());
        if (fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        }else{
            daoPC.eliminar(ids);
        }
    }
    
    //Nuevo
    void nuevo(){
        cboCliente.setSelectedItem("");
        cboEmpleado.setSelectedItem("");
        txtFechaPedido.setText("");
        txtPrecio.setText("");
        txtProducto.setText("");
        txtCantidad.setText("");
        txtDescuento.setText("");
        
    }
    
    //Actualizar
    void actualizar(){
        int Fila=tablaPC.getSelectedRow();
        if(Fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        }else{
            String id_Cli = cboCliente.getSelectedItem().toString();        
            String id_Usuario = cboEmpleado.getSelectedItem().toString();
            String fecEmi = txtFechaPedido.getText();
            String prePedC = txtPrecio.getText();
            String producto = txtProducto.getText();
            String cantPedCli = txtCantidad.getText();
            String desc = txtDescuento.getText();
            Object[] obj = new Object[8];       
            obj[0] = id_Cli;
            obj[1] = id_Usuario;
            obj[2] = prePedC;
            obj[3] = producto;
            obj[4] = desc;
            obj[5] = cantPedCli;
            obj[6] = fecEmi;
            obj[7] = id_PC;
            daoPC.actualizar(obj);            
        }        
    }
    
    void limpiarTabla(){
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i=i-1;
        }
    }
    
