    void listar(){
        List<PedidoComprobante> lista = daoPC.listar();
        modelo = (DefaultTableModel)tablaPC.getModel();
        Object[] ob= new Object[7];
        for (int i = 0; i < lista.size(); i++) {
            ob[0]=lista.get(i).getId_PC();
            ob[1]=lista.get(i).getPrePedC();
            ob[2]=lista.get(i).getCantPedCli();
            ob[3]=lista.get(i).getDesc();
            ob[4]=lista.get(i).getFecEmi();
            ob[5]=lista.get(i).getId_Cli();
            ob[6]=lista.get(i).getId_Usua();
            //Mostramos el nombre de la tabla de CategoriaProductos
            ob[7]=lista.get(i).getNomCliente();
            modelo.addRow(ob);
        }
        tablaPC.setModel(modelo);
    }
